function implementBubbleSortAlgorithm(array){
  const resultado = array;
  let aux;
  for(let i=0; i<array.length; i++){
    for(let j=0; j<array.length; j++){
      if(resultado[i]<resultado[j]){
        aux= resultado[i];
        resultado[i]=resultado[j];
        resultado[j]=aux;
      }
    }
  }
  return resultado;
}

