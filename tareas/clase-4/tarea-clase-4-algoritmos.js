function countWords(string){
  const newString = string.trim();
  let counter = 1;
  for(let i=0; i<newString.length;i++){
    if (newString[i]!==newString[i+1]&&newString[i]===' '){
      counter++;
    }
  }
  return newString.length==0?0:counter;
  
}
