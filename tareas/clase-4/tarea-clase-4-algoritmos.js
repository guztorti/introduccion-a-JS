function calcularSerieFibonacci(cantidad=10){
    const resultado = [1,1]
    let anterior=1, previo=1;

    for(let i=2; i<cantidad; i++){
        resultado.push(anterior+previo);
        previo = anterior;
        anterior = resultado[i];
    }
    return resultado;
}

